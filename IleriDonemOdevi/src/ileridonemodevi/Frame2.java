    private void jComboBox1İtemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1İtemStateChanged
        // TODO add your handling code here:
          model.clear();
        for (Student s : db.std.values()) {
            for (int i = 0; i < s.courses.length; i++) {
                if(s.courses[i].equals(jComboBox1.getSelectedItem()))
             {
                 model.addElement(s.name);
             }
            }
             
        }
       
        jList1.setModel(model);
        
    }//GEN-LAST:event_jComboBox1İtemStateChanged

    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        
       if(evt.getClickCount() == 2)
       {
           for (Student s : db.std.values()) {
               if(s.name == jList1.getSelectedValue())
               {
                   int top = s.courses.length;
                   dtm.addRow(new Object[]{s.id,s.name,s.gpa,top});
               }
           }
       }
    }//GEN-LAST:event_jList1MouseClicked

    private void jTextField2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField2ActionPerformed
        // TODO add your handling code here:
        model2.clear();
        jComboBox2.removeAllItems();
        
        for (Student s : db.std.values()) 
        {
            if(s.id == Integer.parseInt(jTextField2.getText()))
            {
                for (int i = 0; i < s.courses.length; i++) {
                    model2.addElement(s.courses[i]);
                    jComboBox2.addItem(s.courses[i]);
                }
            }
           
        }
          
        jList2.setModel(model2);
        
    }//GEN-LAST:event_jTextField2ActionPerformed

