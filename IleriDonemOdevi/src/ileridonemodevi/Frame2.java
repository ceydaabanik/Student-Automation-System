    private void jComboBox2İtemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox2İtemStateChanged
        // TODO add your handling code here:
        model3.clear();
        for (Course c : db.crs.values()) {
            if(c.name.equals(jComboBox2.getSelectedItem()) )
            {
                for (int i = 0; i < c.rooms.length; i++) {
                    model3.addElement(c.rooms[i]);
                }
            }
        }
        
        jList3.setModel(model3);
    }//GEN-LAST:event_jComboBox2İtemStateChanged

